package kr.ac.kopo.model;

public class Festival {
    private int festivalNum;
    private String festivalTitle;
    private String festivalDetail;

    public int getFestivalNum() {
        return festivalNum;
    }

    public void setFestivalNum(int festivalNum) {
        this.festivalNum = festivalNum;
    }

    public String getFestivalTitle() {
        return festivalTitle;
    }

    public void setFestivalTitle(String festivalTitle) {
        this.festivalTitle = festivalTitle;
    }

    public String getFestivalDetail() {
        return festivalDetail;
    }

    public void setFestivalDetail(String festivalDetail) {
        this.festivalDetail = festivalDetail;
    }
}
